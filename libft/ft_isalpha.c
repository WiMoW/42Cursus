int ft_isalpha(int c)
{
    int is_alpha;

    is_alpha = 0;
    if (((c >= 'a') && (c <= 'z')) || ((c >= 'A') && (c <= 'Z')))
        is_alpha = 1;
    else
        is_alpha = 0;
    return (is_alpha);
}
